<h2>L'impala</h2>
<img src="https://upload.wikimedia.org/wikipedia/commons/thumb/3/34/Serengeti_Impala3.jpg/290px-Serengeti_Impala3.jpg" alt="Impala">
<p>
    Les impalas font partie de la famille des antilopes, ils ressemblent aux kobs, gazelles et aux cerfs,
    ils mesurent généralement de 1,10 m à 1,50 m de longueur. Les mâles mesurent de 85 à 95 cm de hauteur
    à l’épaule, les femelles mesurent de 75 à 85 cm de hauteur au garrot. Les mâles ont une masse plus
    élevée selon les individus ; de 45 à 75 kg (60 en moyenne) contre 35 à 55 kg (40 en moyenne) pour
    les femelles. Le mâle, comme la femelle, est d’un brun rougeâtre sur le dos et beige sur les côtés.
    Le ventre de l’impala de même que ses lèvres et sa queue sont blancs.

    Il faut aussi mentionner leurs lignes noires uniques à chaque individu au bout des oreilles, sur
    le dos de la queue, le haut des cuisses et sur le front. Ces lignes noires sont très utiles aux
    impalas puisque ce sont des signes qui leur permettent de se reconnaître entre eux. Au Kenya, les
    pisteurs appellent ces animaux "cent onze" (111) à cause des 3 lignes noires des cuisses et de la
    queue visibles lorsque l'impala est de dos.

    Les impalas mâles et femelles ont une morphologie différente. En effet, on peut facilement distinguer
    un mâle par ses cornes en forme de S qui mesurent de 40 à 90 cm de long, la femelle en étant dépourvue.
</p>